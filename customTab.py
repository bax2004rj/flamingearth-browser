import tkinter
from tkinter import ttk
import tkinterweb

class customTab(ttk.Notebook):
    ## A tkinterweb Notebook with close buttons on each tab, made with example from https://stackoverflow.com/questions/39458337/is-there-a-way-to-add-close-buttons-to-tabs-in-tkinter-ttk-notebook

    __initialized = False
    def __init__(self, *args, **kwargs):
        ## Custom notebook with close buttons
        if not self.__initialized:
            self.__initialize_custom_style()
            self.__inititialized = True
        kwargs["style"] = "CustomNotebook"
        ttk.Notebook.__init__(self, *args, **kwargs)
        self._active = None

        self.bind("<ButtonPress-1>", self.on_close_press, True)
        self.bind("<ButtonRelease-1>", self.on_close_release)

        # New tab button
        self.newtab = ttk.Button(self, text="+")
        self.newtab.place(relx=1.0, rely=0.0, anchor='ne', x=-5, y=5)

    ## Bind newtab to command, since *args and **kwargs doesn't like having a "command" parameter next to it
    def bind_newtab(self,command):
        self.newtab.configure(command=command)
    
    def on_close_press(self, event):
        """Called when the button is pressed over the close button"""

        element = self.identify(event.x, event.y)

        if "close" in element:
            index = self.index("@%d,%d" % (event.x, event.y))
            self.state(['pressed'])
            self._active = index
            return "break"

    def on_close_press(self, event):
        """Called when the button is pressed over the close button"""

        element = self.identify(event.x, event.y)

        if "close" in element:
            index = self.index("@%d,%d" % (event.x, event.y))
            self.state(['pressed'])
            self._active = index
            return "break"

    def on_close_release(self, event):
        """Called when the button is released"""
        if not self.instate(['pressed']):
            return

        element =  self.identify(event.x, event.y)
        if "close" not in element:
            # user moved the mouse off of the close button
            return

        index = self.index("@%d,%d" % (event.x, event.y))

        if self._active == index:
            self.forget(index)
            self.event_generate("<<NotebookTabClosed>>")

        self.state(["!pressed"])
        self._active = None

